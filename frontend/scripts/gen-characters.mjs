// Scans public/characters/pipoya/** and emits lib/characters.generated.ts —
// a static catalog of every Pipoya sprite so all source characters are selectable.
// Re-run after adding/removing sprite PNGs: `npm run gen:characters`.
//
// Every Pipoya sheet is a uniform 3-column (walk frames) x 4-row (down/left/right/up)
// grid, so the front-facing idle frame is always column 1, row 0. The catalog only
// needs the file path + a label; the avatar component knows the 3x4 layout.

import { readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";
import { dirname } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const PUBLIC_DIR = join(__dirname, "..", "public");
const ROOT = join(PUBLIC_DIR, "characters", "pipoya");
const OUT = join(__dirname, "..", "lib", "characters.generated.ts");

// folder (relative to a pack root) -> { code, ko, order }
const CATEGORY = {
  "Male": { code: "m", ko: "남성", order: 1 },
  "Female": { code: "f", ko: "여성", order: 2 },
  "Soldier": { code: "s", ko: "군인", order: 3 },
  "Japanese school characters/school uniform 1": { code: "su1", ko: "교복 1", order: 4 },
  "Japanese school characters/school uniform 2": { code: "su2", ko: "교복 2", order: 5 },
  "Japanese school characters/school uniform 3": { code: "su3", ko: "교복 3", order: 6 },
  "Japanese school characters/school uniform 4": { code: "su4", ko: "교복 4", order: 7 },
  "Japanese school characters/teachers": { code: "tch", ko: "교사", order: 8 },
  "__NEKONIN__": { code: "nk", ko: "고양이닌자", order: 9 },
  "Animal": { code: "a", ko: "동물", order: 10 },
  "Xmas": { code: "x", ko: "크리스마스", order: 11 },
  "Enemy": { code: "e", ko: "몬스터", order: 12 },
  "Boss": { code: "b", ko: "보스", order: 13 },
  "Other": { code: "o", ko: "기타", order: 14 },
};

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (name.toLowerCase().endsWith(".png")) out.push(p);
  }
  return out;
}

function slug(s) {
  return s
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

// strip noise prefixes + a redundant leading category word, leaving a short tail.
function fileTail(base, code) {
  let s = base
    .replace(/^pipo-charachip[_-]?/i, "")
    .replace(/^pipo-xmaschara/i, "")
    .replace(/^pipo-nekonin/i, "")
    .replace(/^pipo-/i, "");
  let t = slug(s);
  // drop a leading word identical to the category for the obvious ones
  const redundant = { m: "male", f: "female", s: "soldier", e: "enemy", b: "boss" };
  const word = redundant[code];
  if (word && t.startsWith(word + "-")) t = t.slice(word.length + 1);
  return t || "01";
}

const files = walk(ROOT);
const entries = [];
const seen = new Set();

for (const abs of files) {
  const rel = relative(ROOT, abs).split("\\").join("/");
  const segs = rel.split("/");
  const pack = segs[0];
  const isNekonin = /NEKONIN/i.test(pack);
  // category key = folder path inside the pack (excluding the file)
  const inner = segs.slice(1, -1).join("/");
  const key = isNekonin ? "__NEKONIN__" : inner;
  const cat = CATEGORY[key];
  if (!cat) {
    console.warn("⚠️  uncategorized, skipped:", rel);
    continue;
  }
  const base = segs[segs.length - 1].replace(/\.png$/i, "");
  const tail = fileTail(base, cat.code);
  let id = `${cat.code}-${tail}`;
  let n = 2;
  while (seen.has(id)) id = `${cat.code}-${tail}-${n++}`;
  seen.add(id);

  const src = "/characters/pipoya/" + rel; // path relative to /public
  const label = `${cat.ko} ${tail}`;
  entries.push({ id, label, category: cat.ko, order: cat.order, src });
}

entries.sort((a, b) => a.order - b.order || a.id.localeCompare(b.id));

const tooLong = entries.filter((e) => e.id.length > 40);
if (tooLong.length) {
  console.error("❌ ids exceed 40 chars (backend limit):", tooLong.map((e) => e.id));
  process.exit(1);
}

const body = entries
  .map((e) => `  { id: ${JSON.stringify(e.id)}, label: ${JSON.stringify(e.label)}, category: ${JSON.stringify(e.category)}, src: ${JSON.stringify(e.src)} },`)
  .join("\n");

const out = `// AUTO-GENERATED by scripts/gen-characters.mjs — do not edit by hand.
// ${entries.length} Pipoya sprites. Each sheet is a 3-col x 4-row walk grid;
// the avatar renders the front-idle frame (col 1, row 0).

export interface CharacterDef {
  id: string;
  label: string;
  category: string;
  src: string;
}

/** Frames per sheet: 3 walk columns, 4 direction rows. Front-idle = (col 1, row 0). */
export const SHEET_COLS = 3;
export const SHEET_ROWS = 4;
export const IDLE_COL = 1;
export const IDLE_ROW = 0;

export const CHARACTERS: CharacterDef[] = [
${body}
];
`;

writeFileSync(OUT, out, "utf8");
console.log(`✅ wrote ${entries.length} characters -> ${relative(join(__dirname, ".."), OUT)}`);
